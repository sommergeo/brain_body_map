function exp_Br1(context) {
    // layer = 'Brain_size_data' AND "Age group" = 1

    var feature = context.feature;
    
    if (feature.properties) {
        return ((feature.properties['layer']  == 'Brain_size_data') && (feature.properties['Age group']  == 1));
    } else {
        return ((feature['layer']  == 'Brain_size_data') && (feature['Age group']  == 1));
    }
}


